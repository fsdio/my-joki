    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        boolean hasil = ConnectDB.cekDataEmail("tb_user", txt_login.getText(), "email");
        String email = "example@gmail.com";
        String pass = "SALAH";
        MyData data = new MyData();
        try {
            String sql = "select email, password from tb_user where email='"+ txt_login.getText() +"'";
            java.sql.Connection conn = (Connection)ConnectDB.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while(res.next()){
                email = res.getString(1);
                pass = res.getString(2);
            }
        } catch (SQLException e) {
            System.out.println("GAGAL DICEK !!!");
        }
        
        if (hasil==false) {
            JOptionPane.showMessageDialog(rootPane, "DATA EMAIL KAMU BELUM TERDAFTAR !!!");
            new Registrasi().setVisible(true);
            this.dispose();
        }else if( !String.valueOf(txt_password.getPassword()).equals(pass)){
                JOptionPane.showMessageDialog(rootPane, "PASSWORD SALAH !!!");
        }else if( txt_login.getText().equals(email)&& String.valueOf(txt_password.getPassword()).equals(pass)){
            if(email.equals("admin@gmail.com")){
                data.setEmail(email);
                new Admin.Home().setVisible(true);
                this.dispose();
            }else{
                data.setEmail(email);
                new User.Home().setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_btn_loginActionPerformed


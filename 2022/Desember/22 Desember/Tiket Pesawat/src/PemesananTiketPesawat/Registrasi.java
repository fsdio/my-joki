    private void btn_daftarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_daftarActionPerformed
        // TODO add your handling code here:
        boolean hasil = ConnectDB.cekDataEmail("tb_user", txt_email.getText(), "email");
        if(hasil == true ){
            JOptionPane.showMessageDialog(rootPane, "DATA EMAIL KAMU SUDAH TERDAFTAR !!!");
        }else{
            try {
                MyData data = new MyData();
                data.setEmail(txt_email.getText());
                String sql = "INSERT INTO `tb_user` (`nama_lengkap`, `email`, `no_hp`, `password`, `alamat`) VALUES ('"+txt_nama.getText()+"','"+txt_email.getText()+"','"+txt_nohp.getText()+"','"+String.valueOf(txt_password.getPassword())+"','"+txt_alamat.getText()+"')";
                java.sql.Connection conn = ConnectDB.configDB();
                java.sql.PreparedStatement pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
                new Login().setVisible(true);
                this.dispose();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
        
    }//GEN-LAST:event_btn_daftarActionPerformed


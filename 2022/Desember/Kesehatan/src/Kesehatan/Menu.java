    private void PJlapanganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PJlapanganActionPerformed
        // TODO add your handling code here:
        String alamat_lapangan = (String) PJlapangan.getSelectedItem();
        Integer selectItem = PJtable.getSelectedRowCount();
        Integer row = PJtable.getSelectedRow();
        if(selectItem == 0){
            JOptionPane.showMessageDialog(rootPane, "PILIH SALAH SATU DATA");
        }else{
            try {
                String sql = "SELECT alamat_lapangan, harga FROM tb_jadwal WHERE nama_lapangan='"+alamat_lapangan+"'";
                java.sql.Connection conn = config.configDB();
                java.sql.Statement st = conn.createStatement();
                java.sql.ResultSet rs = st.executeQuery(sql);

                while(rs.next()){
                    PJalamat.setText(rs.getString(1));
                    PJharga.setText(rs.getString(2));
                }
                Integer harga = Integer.parseInt(PJharga.getText());
                String jam = PJtable.getValueAt(row, 1).toString();
                Integer total = harga*Integer.parseInt(jam);
                PJtotal.setText(total.toString());
            } catch (NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(rootPane, "ERROR DATABASE" + e);
            }
            
            DefaultTableModel dm = (DefaultTableModel) PJtable.getModel();
            int rowCount = dm.getRowCount();
            //Remove rows one by one from the end of the table
            for (int i = rowCount - 1; i >= 0; i--) {
                dm.removeRow(i);
            }
        }
        
    }//GEN-LAST:event_PJlapanganActionPerformed


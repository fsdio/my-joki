    public void getLapangan(){
        try {
            String sql = "SELECT nama_lapangan FROM tb_jadwal";
            java.sql.Connection conn = config.configDB();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                PJlapangan.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "ERROR DATABASE");
        }
    }
    
    public boolean getValidatePL(){
        if(PLpesan.getText().equals("")){
            return false;
        }else if(PLjam.getValue().equals(0)){
            return false;
        }else if(PLdate.getSelectedItem().equals("Pilih Tanggal")){
            return false;
        }
        return true;
    }
    
    public void getPLtanggal(){
        Date date = new Date();
        SimpleDateFormat SDF = new SimpleDateFormat("YYYY-MM-dd");
        SimpleDateFormat tahun = new SimpleDateFormat("YYYY");
        Calendar cal = new GregorianCalendar(Integer.parseInt(tahun.format(date)), date.getMonth(), date.getDate());
        for (int i = 1; i <= 7; i++) {
           cal.add(Calendar.DAY_OF_MONTH, 1);
           PLdate.addItem(SDF.format(cal.getTime()));
           PJtanggal.addItem(SDF.format(cal.getTime()));
        }
    }
    

            public void date(){
        new Thread(){
            int waktumulai;
            @Override
            public void run(){
                while (waktumulai==0){
                    Calendar c = new GregorianCalendar();
                    int tanggal = c.get(Calendar.DATE);
                    int bulan = c.get(Calendar.MONTH);
                    int tahun = c.get(Calendar.YEAR);
                    String time = tanggal+" : "+(bulan + 1)+" : "+tahun;
                    txtTanggal.setText(time);
                }
            }
        }.start();
    }
    public void waktu(){
        new Thread(){
            int waktumulai;
            @Override
            public void run(){
                while (waktumulai==0){
                    Calendar c = new GregorianCalendar();
                    int jam = c.get(Calendar.HOUR);
                    int menit = c.get(Calendar.MINUTE);
                    int detik = c.get(Calendar.SECOND);
                    int PMAM = c.get(Calendar.AM_PM);
                    String SM;
                    if( PMAM == 1 ){
                        SM = "PM";
                    }else{
                        SM = "AM";
                    }
                    String time = jam+" : "+menit+" : "+detik + " " + SM;
                    txtJam.setText(time);
                }
            }
        }.start();
    }

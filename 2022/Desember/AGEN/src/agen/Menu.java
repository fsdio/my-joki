    private void btnPBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPBayarActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter tglFormat = DateTimeFormatter.ofPattern("dd MMMM yyyy");
        LocalDate tgl = LocalDate.parse(txtTanggal.getText(),tglFormat);
        Long id_user;
        id_user = Long.parseLong(txtID.getText());
        String pulsa = "pls";
        Double nominal;
        nominal = Double.parseDouble(txtPNominal.getText());
        String status = "Berhasil";
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection(Global.connectionUrl,Global.userName,Global.pass);
            Statement stm = con.createStatement();
            String sql = "Insert into transaksi(id_user, tanggal , id_produk , number, nominal ,status)" + "values" +"('"+id_user+"', "+"'"+tgl.toString()+"','"+pulsa+"' , '"+txtPNo.getText()+"', '"+nominal+"', '"+status+"')";
            int Simpan = stm.executeUpdate(sql);
            if(Simpan>0)
            {
                new Transaksi().setVisible(true);
            
            }
        }catch (Exception ex){
            JOptionPane.showMessageDialog(this, "Transaksi Gagal", "Pesan", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_btnPBayarActionPerformed


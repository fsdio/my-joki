    private void btnAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAccountActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con = DriverManager.getConnection(Global.connectionUrl,Global.userName,Global.pass);
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery("select * from user where username = '"+txtNama2.getText()+"'");
            
            while(rs.next())
            {
                txtID.setText(rs.getString("id_user"));
                txtUser.setText(rs.getString("username"));
                txtNama.setText(rs.getString("name_user"));
                txtNo1.setText(rs.getString("no_handphone"));
                txtPass.setText(rs.getString("password"));
            }
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "GAGAL");
        }
    }//GEN-LAST:event_btnAccountActionPerformed

